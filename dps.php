<p>DATA PRIVACY STATEMENT / CONSENT 
The Public Employment Service Office (PESO) of the
Municipality of Mabini, Batangas recognizes our responsibilities under the
Republic Act No. 10173 (Act), also known as the Data Privacy Act of 2012, with
respect to the data we collect, record, organize, update, use, consolidate or
destruct from the jobseekers. The personal data obtained from this form is
entered and stored within the office’s authorized information and
communications system and will only be accessed by the PESO Mabini personnel.
The PESO Mabini have instituted appropriate organizational, technical and
physical security measures to ensure the protection of the jobseeker’s personal
data.</br>

Furthermore, the information collected and stored in the form shall only be
used for the following purposes:</br></br>

1. Job application and/or referral to PESO Mabini partner establishments.</br></br>

2. Reporting of data to the Department of Labor and Employment (DOLE).</br></br>

3. For Labor Market Information (LMI) data collection.</br></br>

PESO Mabini shall not disclose the job seekers’ personal information without
their consent and shall retain this information over a period of ten years for
LMI purposes.</p>
